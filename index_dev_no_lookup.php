    $.fn.extend({
      sortSelect: function sortSelect() {
        var options = this.find("option"),
            arr = options.map(function (_, o) {
              return {
                t: $(o).text(), v: o.value };
            }
                             ).get();
        arr.sort(function (o1, o2) {
          // sort select
          var t1 = o1.t.toLowerCase(),
              t2 = o2.t.toLowerCase();
          return t1 > t2 ? 1 : t1 < t2 ? -1 : 0;
        }
                );
        options.each(function (i, o) {
          o.value = arr[i].v;
          $(o).text(arr[i].t);
        }
                    );
      }
    }
